    private void abrirFormularioInterno(JInternalFrame f){
        
        if (area.getAllFrames().length == 0){
        area.add(f);
        
        //centralizar o formulario
        Dimension d1 = area.getSize();
        Dimension d2 = f.getSize();
        
        f.setLocation(
            (d1.width-d2.width) /2,
             (d1.height-d2.height) /2
        );
        
        f.setVisible(true);
        
        }else{
            JOptionPane.showMessageDialog(null, "Você precisa fechar a janela atual"
                    + "antes de abrir a nova janela seu pau na zorba PABLA VITTTTTTAR");
        }
        
       
    }
    
  
