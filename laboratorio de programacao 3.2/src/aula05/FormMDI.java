    private void menuItemImportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItemImportarActionPerformed
        
        JFileChooser dlg = new JFileChooser();
        dlg.setDialogTitle("Selecione o arquivo");
        dlg.setDialogType(JFileChooser.OPEN_DIALOG);
        dlg.setFileSelectionMode(JFileChooser.FILES_ONLY);
        
        if(dlg.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
        
            File nomearquivo = dlg.getSelectedFile().getAbsoluteFile();
        
            try{
                FileReader fr = new FileReader(nomearquivo);
                BufferedReader br = new BufferedReader(fr);
                String linha = "";
                while((linha = br.readLine())!=null){
                    String dados[] = linha.split(";");
                    Aluno a = new Aluno(
                        Integer.parseInt(dados[0]),
                        dados[1],
                        dados[2]
                    );
                    lista.add(a);
                }
                br.close();
            }catch (Exception e){
                System.out.println("ERRO: " + e.getMessage());
            }
        
                JOptionPane.showMessageDialog(null, "Importação realizada com sucesso!");
            }
    }//GEN-LAST:event_menuItemImportarActionPerformed

