    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (txtId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null,
                    "Utilize o menu PESQUISAR para selecionar \no registro desejado.",
                    "Controle Estoque", JOptionPane.OK_OPTION);
        } else {
            statusControles(true);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        statusControles(false);

        Marca e = new Marca();
        e.setRazaosocial(txtRazaoSocial.getText());
        e.setNomefantasia(txtNomeFantasia.getText());
        e.setCnpj(txtCnpj.getText());

        int res = -1;
        if (txtId.getText().isEmpty()) {
            res = new MarcaDAO().inserir(e);
        }else{
            e.setId(Integer.parseInt(txtId.getText()));
            res = new MarcaDAO().atualizar(e);
        }

        if (res > 0) {
            txtId.setText(Integer.toString(res));
            JOptionPane.showMessageDialog(null,
                    "Opera√ß√£o realizada com sucesso!", "Controle Estoque",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null,
                    "Ocorreu um erro durante a opera√ß√£o.", "Controle Estoque",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

