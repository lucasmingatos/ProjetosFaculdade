    private void configurarFormulario() {
        this.setTitle("VISUALIZAR PEDIDOS");
        this.setMaximizable(false);
        this.setIconifiable(false);
        this.setClosable(true);
        
        configurarTabela();
        preencherTabela();
        
    }    
    
    private void configurarTabela(){
        DefaultTableModel m = new DefaultTableModel(){
              @Override
                public boolean isCellEditable(int row, int column){
                     return false;
        }
    };
        
        m.addColumn("Cliente");
        m.addColumn("Vendedor");
        m.addColumn("Produto");
        m.addColumn("Quantidade");
        m.addColumn("Valor UnitÃ¡rio");
        tabPedidos.setModel(m);
    }
    
    private void preencherTabela(){
        
        if(FormPrincipal.lista != null){
            if(FormPrincipal.lista.size() > 0){
                DefaultTableModel m = (DefaultTableModel)
                        tabPedidos.getModel();
                
                for(Pedido a : FormPrincipal.lista){
                    m.addRow(
                        new Object[]{
                            
                            a.getNome(),
                            a.getVendedor(),
                            a.getProduto(),
                            a.getQuantidade(),
                            a.getValor(),
                        }
                    );
                }
                tabPedidos.setModel(m);
            }
        }
    }
    
